    "Мне сильно запомнилась сцена ближе к началу книги, когда Робинзон составлял список зла, которое свалилось ему на голову. Так, он включил туда то, что он был заброшен судьбой на необитаемый остров, что он отделен от всего мира и обречен на горе и одиночество, что он беззащитен против нападения людей и зверей, что вскоре ему будет даже нечем прикрыть свое тело. Однако на каждое зло, что выпало на его долю, Робинзон смог найти и что-то хорошее: он остался жив, причем единственный из всего экипажа, он не умер от голода, живет в приятном климате, на острове нет диких зверей. Более того, силой Божественного чуда он заполучил огромное количество нужных вещей с корабля, которые дали ему возможность жить на острове в гораздо более лучших условиях, чем те, на которые он мог бы рассчитывать. В этой сцене, в процессе написания этой бухгалтерской книги доходов и расходов, разум Робинзона начинает брать верх над его чувствами и он понимает, что независимо от ситуации, в которую попадает человек (а ситуация Робинзона действительно довольно плачевна), он всегда может найти в своем положении что-то хорошее. Здесь очень ярко раскрывается просветительская идея о господстве разума над чувствами, которая после еще не раз помогает Робинзону не впасть в отчаяние и не натворить чего-то, что привело бы к его печальному концу."
